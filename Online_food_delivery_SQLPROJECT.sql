 --Q19. What is the total orders and amount spent for each member before they become a member ?
 SELECT g.userid,un.user_name,
 COUNT(s.sales_id) AS total_orders_before_gold,
 SUM (p.price) AS total_amount_spent_before_gold
 FROM goldusers_signup g
 JOIN sales s
 ON g.userid=s.userid
 JOIN products p
 ON p.product_id=s.product_id
 JOIN user_name un
 ON g.userid=un.userid
 WHERE s.created_date<g.gold_signup_date
 GROUP BY g.userid,un.user_name
 ORDER BY total_amount_spent_before_gold DESC;

