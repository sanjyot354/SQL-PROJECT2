--Q16. Which item was most popular for each customer ?
SELECT 
userid,
user_name,
product_name,
purchase_count
FROM (
SELECT s.userid,
un.user_name,
p.product_name,
COUNT(*) AS purchase_count,
ROW_NUMBER()OVER(PARTITION BY s.userid
                 ORDER BY COUNT(*)DESC) 
				 AS rv
FROM sales s
JOIN products p
ON s.product_id=p.product_id
JOIN user_name un
ON s.userid=un.userid
GROUP BY s.userid,un.user_name,p.product_name
)t
WHERE rv =1 
ORDER BY userid;

/*Q17. Which item was purchase first by the customer after they 
become a member ? */
SELECT 
userid,
user_name,
product_name,
created_date
FROM (
SELECT 
s.userid,
un.user_name,
p.product_name,
s.created_date,
ROW_NUMBER()OVER (PARTITION BY s.userid ORDER BY s.created_date)
 AS rv
 FROM sales s
 JOIN goldusers_signup g ON s.userid=g.userid
 JOIN products p ON s.product_id=p.product_id
 JOIN user_name un ON s.userid=un.userid
 WHERE s.created_date>= g.gold_signup_date
 )s
 WHERE rv =1
 ORDER BY userid;

