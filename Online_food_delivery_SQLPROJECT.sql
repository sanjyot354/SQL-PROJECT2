--QUERIES
--Q1. What is the total sales revenue generated by each product? 
SELECT 
    p.product_name,
    COUNT(s.sales_id) AS total_sales,
    SUM(p.price) AS total_sales_revenue
FROM sales s
JOIN products p
    ON s.product_id = p.product_id
GROUP BY p.product_name
ORDER BY total_sales_revenue DESC;

--Q2. Which 3 product has the highest sales revenue?
SELECT TOP 3
 p.product_name ,
 COUNT(s.sales_id) AS total_sales,
 SUM(p.price) AS highest_sales_revenue
 FROM sales s
 JOIN products p
      ON s.product_id= p.product_id
	  GROUP BY p.product_name
	  ORDER BY highest_sales_revenue DESC;

--Q3. How many users have signed up for the service and has taken the gold membership?
SELECT 
      (SELECT COUNT(*) FROM users) AS total_users,
	  (SELECT COUNT(*) FROM goldusers_signup) AS gold_users;
	  
--Q4. What is the revenue generated from gold users? 
SELECT 
      SUM(p.price) AS gold_users_revenue
FROM sales s
JOIN products p
ON s.product_id=p.product_id
JOIN goldusers_signup g
ON s.userid=g.userid;

--Q5. What is the total revenue generated from gold users?
SELECT 
SUM(p.price) AS total_revenue_goldusers
FROM sales s
JOIN products p
ON s.product_id=p.product_id
JOIN goldusers_signup g
ON s.userid=g.userid;

--Q6. Which users has been a gold user for the How much of time?
SELECT 
u.userid,
un.user_name,
g.gold_signup_date,
DATEDIFF(YEAR,g.gold_signup_date,GETDATE()) AS years_as_gold_member,
DATEDIFF(MONTH,g.gold_signup_date,GETDATE()) AS months_as_gold_member
FROM goldusers_signup g
JOIN users u
ON g.userid =u.userid
JOIN user_name un
ON u.userid=un.userid
ORDER BY g.gold_signup_date;

--Q7. What is the most popular product among gold users? 
SELECT TOP 1
p.product_name,
COUNT(s.sales_id) AS total_orders
FROM sales s
JOIN products p
ON s.product_id=p.product_id
JOIN goldusers_signup g
ON s.userid=g.userid
GROUP BY p.product_name
ORDER BY total_orders DESC;

--Q8. What is the total sales revenue generated in each year? 
SELECT 
YEAR (s.created_date) AS sales_year,
SUM (p.price) AS total_sales_revenue
FROM sales s 
JOIN products p
ON s.product_id = p.product_id
GROUP BY YEAR(s.created_date)
ORDER BY sales_year;

--Q9. How has the sales revenue changed over the years? 
SELECT 
sales_year ,
total_revenue,
total_revenue -LAG(total_revenue)
OVER (ORDER BY sales_year) AS revenue_change
FROM (
SELECT 
YEAR (s.created_date) AS sales_year,
SUM (p.price) AS total_revenue
FROM sales s
JOIN products p
ON s.product_id=p.product_id
GROUP BY YEAR (s.created_date)
)t;

--Q10. What is the average Gold-signup compare to just sign up for the users? 
SELECT 
COUNT(DISTINCT g.userid)* 100.0/COUNT(DISTINCT u.userid)
AS gold_signup_percentage 
FROM users u
LEFT JOIN goldusers_signup g
ON u.userid=g.userid
;

--Q11. How many gold members users have order how many numbers of time ?
