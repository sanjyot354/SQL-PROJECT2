--Q14. What was the first order purchase by each customer ? 
SELECT 
userid,
user_name,
product_name,
created_date
FROM (
SELECT
s.userid,
un.user_name,
p.product_name,
s.created_date,
ROW_NUMBER ()
OVER (PARTITION BY s.userid ORDER BY s.created_date) AS rn
FROM sales s
JOIN products p
ON s.product_id = p.product_id
JOIN user_name un
ON s.userid =un.userid
)t
WHERE rn =1
ORDER BY userid;

/*Q15. What is the most purchase item on the menu and how many 
